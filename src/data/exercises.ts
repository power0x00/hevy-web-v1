// Helper to get exercises by muscle group
export const getExercisesByMuscleGroup = (group: string) => 
  exercises.filter(e => e.muscleGroup === group)

// Helper to search exercises
export const searchExercises = (query: string) => 
  exercises.filter(e => 
    e.name.toLowerCase().includes(query.toLowerCase()) ||
    e.muscleGroup.toLowerCase().includes(query.toLowerCase())
  )

// Get exercise by ID
export const getExerciseById = (id: string) => 
  exercises.find(e => e.id === id)

